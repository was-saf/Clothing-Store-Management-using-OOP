    public FemaleGarmentInventory(Employee employee) {
        initComponents();
        this.employee = employee;
        try {
            // Create the SQL query
            String tableName = "FemaleGarments";
            String query = "SELECT * FROM " + tableName;

            // Create a PreparedStatement object
            DatabaseConnector db= new DatabaseConnector();
            
            // Execute the query and get the result set
            ResultSet rs = db.runSelect(query);

            // Loop through the result set and populate the table
            DefaultTableModel model = (DefaultTableModel) jTable.getModel();
            model.setRowCount(0); // Clear the existing table data

            while (rs.next()) {
                // Retrieve the data from the result set
                int garmentID = rs.getInt("garmentID");
                String garmentType = rs.getString("garmentType");
                String garmentName = rs.getString("garmentName");
                String garmentSize = rs.getString("garmentSize");
                String garmentColour = rs.getString("garmentColour");
                int garmentUnit = rs.getInt("garmentUnit");
                double garmentPrice = rs.getDouble("garmentPrice");

                // Add the data to the table model
                model.addRow(new Object[]{garmentID, garmentType, garmentName, garmentSize, garmentColour, garmentUnit, garmentPrice});
            }

            // Close the result set, prepared statement, and connection
            rs.close();
        } catch (SQLException e) {
            e.getMessage();
        }        
    }
    
