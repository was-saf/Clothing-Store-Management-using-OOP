    private void updateBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateBtnActionPerformed
        String garmentName = updateName.getText();
        double newPrice = Double.parseDouble(updatePrice.getText());
        int newStock = Integer.parseInt(updateStock.getText());

        try {
            String tableName = "FemaleGarments";
            String query = "UPDATE " + tableName + " SET garmentPrice=?, garmentUnit=? WHERE garmentName=?";

            DatabaseConnector db = new DatabaseConnector();
            PreparedStatement statement = db.prepareStatement(query);

            statement.setDouble(1, newPrice);
            statement.setInt(2, newStock);
            statement.setString(3, garmentName);

            int rowsAffected = statement.executeUpdate();

            if (rowsAffected > 0) {
                JOptionPane.showMessageDialog(this, "Price and stock updated successfully for all items with the same garmentName");
            } else {
                JOptionPane.showMessageDialog(this, "Failed to update price and stock for any items with the specified garmentName");
            }

            statement.close();
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_updateBtnActionPerformed

